def main():
        num1 = int(input('Enter a number: '))
        num2 = int(input('Enter another number: '))
        sumOfNumbers = num1 + num2
        differenceOfNumbers = num1 - num2
        print(num1, '+', num2, '=', sumOfNumbers)
        print(num1, '-', num2, '=', differenceOfNumbers)

if __name__ == "__main__":
        main()